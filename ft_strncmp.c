#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*str1;
	unsigned char	*str2;

	if (!n)
		return (0);
	i = 0;
	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	while (str1[i] == str2[i] && s1[i] != '\0' && s2[i] != '\0' && i < n)
		i++;
	if (i != n)
		return (str1[i] - str2[i]);
	return (0);
}
